#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *s_bis;

	s_bis = (unsigned char *)s;
	while (n--)
	{
		if (*s_bis == (unsigned char)c)
			return (s_bis);
		s_bis++;
	}
	return (NULL);
}
