#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*str_bis;
	unsigned char	c_bis;

	str_bis = str;
	c_bis = c;
	while (n)
	{
		*str_bis++ = c_bis;
		n--;
	}
	return (str);
}
