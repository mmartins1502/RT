#include "libft.h"

void	*ft_memalloc(size_t size)
{
	char	*allocated_mem;

	if ((allocated_mem = malloc(size)) == NULL)
	{
		ft_dprintf(2, "FATAL ERROR : malloc failure\n");
		exit(EXIT_FAILURE);
	}
	ft_bzero(allocated_mem, size);
	return (allocated_mem);
}
